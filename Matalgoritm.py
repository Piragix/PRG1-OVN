# Korv med bröd

# Börja med att leta rätt på allt du behöver. 
# Du kommer att behva: En stekpanna, en spis, någonting att plocka upp korven med, smör eller olja, en korv, ett bröd och valfi topping.
# Börja med att plocka fram ett bröd och en korv. Om korven är fryst så den sitter fast i närliggande korvar kan du läga korven i mikron i några sekunder.
# Om även brödet är fryst så kan du sätta på ugnen för att tina det. Tinar man bröd i mikron blir de sladdriga och sega.
# Sätt på spisen på max värme. Lägg stekpannan på spisplattan och tillför lite smör/olja.
# När smöret har slutat fräsa så ska du lägga korven i pannan. 
# Det är viktigt att korven inte ligger på samma sida hela tiden. Då kan den bli bränd.
# När korven är stekt till din preferens så är det dags att plocka upp den ur pannan. 
# Lägg korven i brödet och tilsätt vald topping. Rekomenderade toppingar är: Ketchup, Senap, Vitlökssås, Rostad lök.
# Du har nu lagat korv med bröd! 